package builderb0y.bigtech.util;

public class NbtReadingException extends Exception {

	public NbtReadingException() {}

	public NbtReadingException(String message) {
		super(message);
	}

	public NbtReadingException(Throwable cause) {
		super(cause);
	}

	public NbtReadingException(String message, Throwable cause) {
		super(message, cause);
	}
}